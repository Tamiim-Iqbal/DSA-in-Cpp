#include<bits/stdc++.h>
using namespace std;
int main()
{
    long long int a,b;
    cin>>a>>b;
    long long int p=a+b;
    long long int m=a*b;
    long long int s=a-b;
    cout<<a<<" + "<<b<<" = "<<p<<endl;
    cout<<a<<" * "<<b<<" = "<<m<<endl;
    cout<<a<<" - "<<b<<" = "<<s<<endl;
    return 0;
}